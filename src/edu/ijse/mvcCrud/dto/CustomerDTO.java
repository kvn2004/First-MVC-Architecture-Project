package edu.ijse.mvcCrud.dto;

/**
 *
 * @author KVN2004
 */
public class CustomerDTO {

    private String id, title, name, address, city, province, postalCode, dob;
    private double salary;

    public CustomerDTO() {
    }

    public CustomerDTO(String id, String title, String name, String address, String city, String province, String postalCode, String dob, double salary) {
        this.id = id;
        this.title = title;
        this.name = name;
        this.address = address;
        this.city = city;
        this.province = province;
        this.postalCode = postalCode;
        this.dob = dob;
        this.salary = salary;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    @Override
    public String toString() {
        return "CustomerDTO{" +"id "+id+"name "+name+"dob "+dob+"title "+title+"address "+address+"city "+city+"province "+province+"postcode "+postalCode+"salary "+salary+"}";
    }
    
    
}
